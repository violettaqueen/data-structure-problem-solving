package java1;

public class Print4WeeksAsANoteBook {
    public static void main(String[] args) {

        for (int i = 1; i < 5; i++) { //4 weeks
            System.out.println("Week :" + i);

            for (int j = 1; j < 8; j++) { //days
                System.out.println("    Day : " + j);
            }
            System.out.println();
        }



    }
}
